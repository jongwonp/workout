// =========================================
// Helper: 매핑 생성
// =========================================

function mapping(
  exerciseId: string,
  muscleId: string,
  coefficient: number
): ExerciseMuscleMapping {
  const role: MuscleRole =
    coefficient >= 1.0
      ? 'primary'
      : coefficient >= 0.5
        ? 'secondary'
        : coefficient > 0
          ? 'minor'
          : 'stabilizer';
  return { exercise_id: exerciseId, muscle_id: muscleId, role, coefficient };
}

