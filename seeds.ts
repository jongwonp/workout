export type MuscleGroup = 'push' | 'pull' | 'legs' | 'core';
export type ExerciseCategory = 'compound' | 'isolation' | 'mobility' | 'warmup';
export type Equipment =
  | 'barbell'
  | 'dumbbell'
  | 'machine'
  | 'cable'
  | 'bodyweight'
  | 'ez_bar';
export type GripType =
  | 'pronated'
  | 'supinated'
  | 'neutral'
  | 'mixed'
  | 'none';
export type SkillLevel = 'easy' | 'medium' | 'hard';
export type Stability = 'stable' | 'moderate' | 'unstable';
export type MuscleRole = 'primary' | 'secondary' | 'minor' | 'stabilizer';

export interface BodyRegion {
  id: string;
  name_ko: string;
  parent_id: string | null;
}

export interface Muscle {
  id: string;
  name_ko: string;
  group: MuscleGroup;
}

export interface Exercise {
  id: string;
  name: string;
  category: ExerciseCategory;
  default_equipment: Equipment;
  is_custom: boolean;
  target_rep_range: [number, number];
  weight_increment_kg: number;
  default_rest_seconds: number;
  recommendation_bias: number;
  fatigue_factor: number;
  joint_stress_region_ids: string[];
  movement_patterns: string[];
  skill_level: SkillLevel;
  stability: Stability;
  unilateral: boolean;
  alternative_group_id: string | null;
}

export interface ExerciseVariation {
  id: string;
  exercise_id: string;
  name: string;
  grip_type: GripType;
  equipment: Equipment;
  is_default: boolean;
  /**
   * 관절 부담 보정. BodyRegion.id를 키로 사용.
   * 1.0 = 기본 부담, 0.5 = 50% 감소, 0 = 부담 없음.
   */
  joint_stress_modifier: Record<string, number>;
}

export interface ExerciseMuscleMapping {
  exercise_id: string;
  muscle_id: string;
  role: MuscleRole;
  coefficient: number;
}

